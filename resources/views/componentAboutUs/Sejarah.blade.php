        <h4 class="fs-4">Sejarah Mirota Kampus</h4>
        Manna Kampus yang sebelumnya dikenal dengan nama Mirota Kampus berdiri pada tanggal 13 Mei 1985 di bawah naungan
        PT. Mirota Nayan sehingga setiap tanggal 13 Mei diperingati sebagai hari jadi Manna Kampus.
        <br><br>
        Pada tahun 1950, Bapak Hendro Sutikno (Tan Kim Tiek) Dan Ibu Tini Yuliati (Nyoo Tien Nio) mendirikan perusahaan
        perseroan dengan nama PT. Mirota. Penggunaan nama Mirota berawal dari kata Mirah Tan. Kata “Mirah” yang berarti Murah
        dan ”Tan” yang berasal dari nama Bapak Hendro Sutikno (Tan Kim Tiek) dan di gabung menjadi Mirah Tan. Dengan berjalannya
        waktu, agar lebih familiar nama tersebut diubah menjadi Mirota yang merupakan akronim dari kata “Minuman, Roti, dan Tart.”
        <br><br>
        Tahun 1952, Bapak dan Ibu Hendro Sutikno juga mengembangkan usaha dengan membuka toko P & D (Provition & Drunken) yang
        terletak di Jl. A. Yani 75, Yogyakarta. Perkembangan usaha PT Mirota, diteruskan oleh putera-puteri Bapak Hendro Sutikno
        meliputi berbagai jenis usaha dengan nama brand "MIROTA".
        <br><br>
        Pada tahun 1980 didirikan sebuah rumah makan dengan nama Mirota Nayan oleh Bapak Siswanto Hendro Sutikno selaku pemilik dan
        Direktur Utama usaha tersebut yang berlokasi di Jl. Solo Km 8 Yogyakarta. Setelah itu berpindah ke Babarsari, Catur Tunggal,
        Sleman yang terletak di Jl. Solo Km 7 Babarsari, Yogyakarta, serta menambah usaha dengan menjual makanan,
        minuman, dan alat tulis.
        <br><br>
        Bapak Siswanto Hendro Sutikno kemudian mengembangkan usaha di bidang retail bersama Bapak Nico Sukandar, yaitu Mirota Kampus.
        Notaris RM. Soeryanto Partaningrat, SH. mengesahkan usaha ini dengan nama “PT. Mirota Nayan” pada tahun 1983, yang aktanya
        ditandatangani oleh Bapak Siswanto Hendro Sutikno sebagai Direktur Utama dan Bapak Nico Sukandar sebagai General Manager.
        <br><br>
        PT. Mirota Nayan memperluas usahanya dengan membuka toko buku dan alat-alat tulis dengan nama “Mirota Kampus” yang
        diresmikan pada tanggal 13 Mei 1985. Momen ini merupakan titik tolak berkembangnya PT. Mirota Nayan dan tanggal 13 Mei
        diperingati sebagai hari jadi Mirota Kampus (PT. Mirota Nayan). Nama Mirota Kampus diambil berdasarkan letak geografis
        toko karena berdekatan dengan beberapa kampus di Kota Yogyakarta (UGM, UNY, dan UII) dan juga beberapa jenjang pendidikan
        umum lainnya yang berada di Jl. C. Simanjuntak No. 64C Yogyakarta.
        <br><br>
        Di mulai pada tahun 2009,  Mirota Kampus mulai berkembang pesat, dimana pada tanggal 19 Desember 2009 Mirota Kampus
        memulai usaha baru yaitu MK Cafe yang berada di komplek Mirota Kampus C. Simanjuntak, Terban, Gondokusuman, D.I. Yogyakarta.
        Kemudian pada 20 Agustus 2010, Mirota Kampus membuka cabang yang terletak di Jl. Menteri Supeno 38 Yogyakarta atau sering
        disebut dengan Mirota Kampus M. Supeno. Selanjutnya pada tanggal 15 Juni 2011 Mirota Kampus kembali lagi mengembangkan outlet
        baru yang terletak di  Jl. Palagan Tentara Pelajar No.31 Yogyakarta (Mirota Kampus Palagan). Di susul kembali pada 1 Januari
        2012, Mirota Kampus membuka cabang baru yang terletak di Jl. Godean Km. 2,8 Yogyakarta (Mirota Kampus Godean).
        <br><br>
        Perkembangan Mirota Kampus semakin pesat pada tahun 2020 ketika Mirota Kampus memulai memperkenalkan nama toko baru,
        yang semula Mirota Kampus di ubah menjadi Manna Kampus. Perubahan nama toko ini bertujuan untuk memperluas area layanan,
        memperbaiki service quality, dan tetap menjadi “Rumah Belanja Terpercaya”. Setelah itu pada 2020—2021 Manna Kampus (Mirota Kampus)
        membuka outlet baru dengan tipe minimarket yaitu Manna Kampus Mini Pelemsewu yang berdiri pada tanggal 28 Agustus 2020.
        Pada tanggal 26 Maret 2021 kembali membuka minimarket yang dikenal dengan Manna Kampus Mini Diro, lalu pada tanggal 9 April 2021
        mengembangkan outlet besarnya yang terletak di Jl. Imogiri Timur Km.7, Grojogan, Wirokerten, Banguntapan, Bantul
        (Manna Kampus Imogiri). Pada tanggal 26 November 2021 membuka cabang baru di Jl. Kakap Raya yang dikenal Manna Kampus Mini
        Minomartani. Sehingga pada saat ini Manna Kampus (Mirota Kampus) memiliki 6 (enam) cabang outlet Manna Kampus, 1 (satu)
        outlet Manna Kampus Peralatan Rumah Tangga dan 3 (tiga) cabang Manna Kampus Mini. Nama Manna Kampus (Mirota Kampus) itu sendiri
        diambil berdasarkan lokasi, karena sangat dekat dengan beberapa kampus di Kota Yogyakarta (UGM, UII, UNY, dan beberapa sekolah).
